ORG 0

Switches:  EQU 000
LEDs:      EQU 001
Timer:     EQU 002
ADC:       EQU 003
HEX_RIGHT: EQU 004
HEX_LEFT:  EQU 005

MAIN:
    IN      Switches
    STORE   SW_RAW
    AND     MASK_SW9
    JZERO   NORMAL_OP       
    LOAD    MODE_INVALID
    OUT     ADC
    IN      ADC             
    OUT     HEX_RIGHT
    JUMP    MAIN            

NORMAL_OP:
    LOAD    SW_RAW
    AND     MASK_SW10
    STORE   TTL_SEL
    JZERO   USE_IN_LO       
    ADDI    -1
    JZERO   USE_OUT_LO      
    ADDI    -1
    JZERO   USE_IN_HI       
    JUMP    USE_OUT_HI     

USE_IN_LO:
    LOAD    CFG_IN_LO
    JUMP    SEND_CONFIG
USE_OUT_LO:
    LOAD    CFG_OUT_LO
    JUMP    SEND_CONFIG
USE_IN_HI:
    LOAD    CFG_IN_HI
    JUMP    SEND_CONFIG
USE_OUT_HI:
    LOAD    CFG_OUT_HI

SEND_CONFIG:
    STORE   CONFIG_WORD
    LOAD    CONFIG_WORD
    OUT     ADC
    LOAD    CONFIG_WORD
    OUT     HEX_RIGHT
    LOAD    TTL_SEL
    OUT     HEX_LEFT
    LOAD    TTL_SEL
    OUT     LEDs
    JUMP    MAIN

; CONSTANTS & DATA
CFG_IN_LO:   DW  &B0000000000000011
CFG_OUT_LO:  DW  &B0000000100000011
CFG_IN_HI:   DW  &B0000001000000011
CFG_OUT_HI:  DW  &B0000001100000011

MODE_INVALID: DW &B0000000000011000

; Switch masks
MASK_SW9:    DW  &B0000001000000000   ; isolate SW9
MASK_SW10:   DW  &B0000000000000011   ; isolate SW[1:0]

; Scratch
SW_RAW:      DW  0
TTL_SEL:     DW  0
CONFIG_WORD: DW  0